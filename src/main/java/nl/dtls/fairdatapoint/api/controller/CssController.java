package nl.dtls.fairdatapoint.api.controller;

import nl.dtls.fairdatapoint.service.scss.ScssService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.ResponseBody;
import org.springframework.web.bind.annotation.ResponseStatus;

@Controller
@RequestMapping("/css")
public class CssController {

    @Autowired
    private ScssService scssService;

    @RequestMapping(value = "/main.css", method = RequestMethod.GET, produces = {"text/css"})
    @ResponseStatus(HttpStatus.OK)
    @ResponseBody
    public String getScss() {
        return scssService.loadScss("main");
    }
}
